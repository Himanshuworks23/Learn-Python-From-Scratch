"""Most Commonly used Arithmatic Operators"""

num1 = int(input("Enter 1st number: "))
num2 = int(input("Enter 2nd number: "))

Sum = num1 + num2
Diff = num1 - num2
Mul = num1 * num2
Exp = num1 ** num2
Div = num1 / num2
Div_Whole = num1 // num2
Modulus = num1 % num2

print(f"Sum={Sum}\n Difference={Diff}\n Product={Mul}\n Power={Exp}\n Division={Div}\n Division_Whole_no={Div_Whole}\n Modulus={Modulus}")

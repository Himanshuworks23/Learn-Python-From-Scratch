"""Most Commonly used Conditional Operators"""

"""Will return true or false as output"""

num1 = int(input("Num1= "))
num2 = int(input("Num2= "))

print(f"Double Equals (==): {num1 == num2}")
print(f"Not Equals (!=): {num1 != num2}")
print(f"Less than (<): {num1 < num2}")
print(f"Less than Equals (<=): {num1 <= num2}")
print(f"Greater than (>): {num1 > num2}")
print(f"Greater than Equals (>=): {num1 >= num2}")